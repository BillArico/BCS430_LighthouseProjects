At the Lighthouse Project, we plan on creating a website that would allow for people and companies to contribute to donation activities and services from other food banks. We will work with these food banks and organizations such as Feeding America and Long Island Cares that would allow them to promote their food drives, soup kitchens, and other activities on our website. As for people, they can sign up on our website or access it as a guest account and have the option to find a food drive from any of these organizations and donate through there. People who have signed up can even volunteer in a soup kitchen or food drive and help out the community. That last part is especially beneficial to high school students who need to do volunteer work for their senior year. We will have our website work with restaurants, grocery stores, and other food services that can also contribute to these activities. We will also try to encourage them to promote this to their employees, whether it’s donating food or convincing them to volunteer and help out. This not only feeds many hungry, needy people and not only does it lower down food wastage, it’ll help bring people and many companies closer together with the community.
